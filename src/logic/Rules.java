package logic;

public abstract class Rules {
	
	/*
	 *  -----------------------------------------------Attributes------------------------------------------------------------
	 */
	protected int boardRowCount ;
	
	protected int boardColumnCount;
	
	/*
	 *  -----------------------------------------------Methodes------------------------------------------------------------
	 */
	
	public int getBoardRowCount(){
		return boardRowCount;
	}

	public int getBoardColumnCount(){
		return boardColumnCount;
	}
}
